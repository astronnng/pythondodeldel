def calcula_quadrado(lista):  #Definindo o que deve ser feito na DEF 
    for elem in lista:
        quad = elem ** 2
        print(f"O quadrado é: {elem} é {quad}")


lista_de_inteiros = [1, 2, 3, 4, 5] #Passando os valores que estao na lista 

calcula_quadrado(lista_de_inteiros) #usando a def a def e passando onde ela tem que ver os valores 

